class HintLearning:
    def __init__(self, device):
        self.mse = torch.nn.MSELoss()
        # self.model = [nn.Conv2d(64, 256, (1, 1)).to(device),
        #               nn.Conv2d(128, 512, (1, 1)).to(device),
        #               nn.Conv2d(256, 1024, (1, 1)).to(device)]
        self.resolution_scaling = [0, 0, 0]

    def __call__(self, teacher_feature, student_feature, index):
        """ YOLOv5 version (17, 20, 23) layers
        teacher_feature: FPN features from teacher model : (20,20) (40,40), (80, 80) size
        student_feature: FPN features from student model : (20,20) (40,40), (80, 80) size
        """

        if not self.resolution_scaling[index]:
            resolution = teacher_feature.size()[-2] * teacher_feature.size()[-1]
            self.resolution_scaling[index] = resolution
        # print(teacher_feature.grad, teacher_feature.requires_grad)
        # print(student_feature.grad, student_feature.requires_grad)
        loss = self.mse(teacher_feature, student_feature)
        # loss.retain_grad()
        # print(loss, loss.grad, loss.requires_grad)
        return loss


