class DecoupleMaskLoss:
    def __init__(self, device):
        self.mse = torch.nn.MSELoss()
        self.device = device

    def __call__(self, teacher_feature, student_feature, mask, r_bg, r_obj):
        """ YOLOv5 version (17, 20, 23) layers
        teacher_feature: FPN features from teacher model : (20,20) (40,40), (80, 80) size
        student_feature: FPN features from student model : (20,20) (40,40), (80, 80) size
        >> (B, C, H, W)
        """
        b, c, feat_th, feat_tw = teacher_feature.shape
        b, c, feat_sh, feat_sw = student_feature.shape
        assert feat_th == feat_sh
        assert feat_tw == feat_sw
        
        r_bg = r_bg.unsqueeze(-1).unsqueeze(-1).to(self.device)
        r_obj = r_obj.unsqueeze(-1).unsqueeze(-1).to(self.device)

        mask_rescaled = F.interpolate(mask.unsqueeze(1), size=(feat_th, feat_tw), mode='bilinear')
        mask_rescaled[mask_rescaled == 0] = 0
        mask_rescaled[mask_rescaled != 0] = 1
        mask_rescaled = mask_rescaled.to(self.device)

        teacher_feat_mask = mask_rescaled * teacher_feature / r_obj
        student_feat_mask = mask_rescaled * student_feature / r_obj

        teacher_feat_mask_bg = (1 - mask_rescaled) * teacher_feature / r_bg
        student_feat_mask_bg = (1 - mask_rescaled) * student_feature / r_bg

        feat_mask_obj_loss = self.mse(teacher_feat_mask, student_feat_mask) 
        feat_mask_bg_loss = self.mse(teacher_feat_mask_bg, student_feat_mask_bg)
        return feat_mask_obj_loss


class PredGuidedLoss:
    def __init__(self, device):
        self.device = device

    def __call__(self, teacher_pred, student_pred, teacher_fpn, student_fpn):
        batch, c, h, w = teacher_fpn.shape
        pred_diff = torch.pow(teacher_pred - student_pred, 2)   # (B, C, H, W)
        pred_diff = torch.mean(pred_diff, dim=1)    # (B, H, W)

        fpn_diff = torch.pow(teacher_fpn - student_fpn, 2)  # (B, C, H, W)
        fpn_diff = torch.mean(fpn_diff, dim=1)  # (B, H, W)

        # print(torch.mul(fpn_diff, pred_diff).reshape(batch, -1).shape)
        PFI_loss = torch.norm(torch.mul(fpn_diff, pred_diff).reshape(batch, -1), dim=-1, p=2)
        # print(PFI_loss)
        PFI_loss = torch.mean(PFI_loss) / (h * w)
        # print(PFI_loss)
        return PFI_loss



class MaskPredGuidedLoss:
    def __init__(self, device):
        self.device = device

    def __call__(self, teacher_pred, student_pred, teacher_fpn, student_fpn, gt_mask):
        # print(teacher_pred.shape, student_pred.shape, teacher_fpn.shape, student_fpn.shape )
        batch, c, h, w = teacher_fpn.shape

        mask_rescaled = F.interpolate(gt_mask.unsqueeze(1), size=(h, w), mode='bilinear').to(self.device)
        mask_rescaled[mask_rescaled == 0] = 0
        mask_rescaled[mask_rescaled != 0] = 1

        teacher_pred = mask_rescaled * teacher_pred
        teacher_fpn = mask_rescaled * teacher_fpn

        student_pred = mask_rescaled * student_pred
        student_fpn = mask_rescaled * student_fpn

        pred_diff = torch.pow(teacher_pred - student_pred, 2)   # (B, C, H, W)
        pred_diff = torch.mean(pred_diff, dim=1)    # (B, H, W)

        fpn_diff = torch.pow(teacher_fpn - student_fpn, 2)  # (B, C, H, W)
        fpn_diff = torch.mean(fpn_diff, dim=1)  # (B, H, W)

        # print(torch.mul(fpn_diff, pred_diff).reshape(batch, -1).shape)
        mask_PFI_loss = torch.norm(torch.mul(fpn_diff, pred_diff).reshape(batch, -1), dim=-1, p=2)
        # print(PFI_loss)
        mask_PFI_loss = torch.mean(mask_PFI_loss) / (h * w)
        return mask_PFI_loss


